package com.example.unitconverter;

public class Metre {
    public static double CalculateCentimetre(double m){
        return m * 100;
    }

    public static double CalcualteFoot(double m){
        return m * 3.28084;
    }

    public static double CalculateInch(double m){
        return m * 39.3701;
    }
}
