package com.example.unitconverter;

public class Kilograms {

    public static double CalculateGram(double kilogram){
        return kilogram * 1000.0;
    }

    public static double CalculateOunce(double kilogram){
        return kilogram * 35.274;
    }

    public static double CalculatePound(double kilogram){
        return kilogram * 2.20462;
    }

}
